package org.tcs;

public class Boxing_Unboxing {
public static void main(String[] args) {
	int a=20;
	System.out.println("Before converting into object datatype :"+a);
	Integer b=a;
	System.out.println("After converting into object datatype :"+b);
	Float x=10.5f;
	System.out.println("Before converting into object datatype :"+x);
	float y=x;
	System.out.println("After converting into object datatype :"+y);
}
}
